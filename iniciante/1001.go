package main
 
import (
    "fmt"
)
 
func main() {  
    //Declaração de variáveis 
    var A, B int 
    
    //Entrada de dados
    fmt.Scanf("%d", &A)
    fmt.Scanf("%d", &B) 
    
    //Cálculo da soma 
    X := A + B
    
    //Saída de dados 
    fmt.Printf("X = %d\n", X)
}
