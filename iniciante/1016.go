//----> 1016. Distância
package main
 
import (
    "fmt"
)
 
func main() {
    //Declaração de variáveis 
    var distancia int
    
    //Entrada de dados
    fmt.Scanf("%d", &distancia)
    
    //Cálculo do tempo 
    tempo := distancia * 2 
    
    //Saída de dados
    fmt.Printf("%d minutos\n", tempo) 
}